    public function store(Request $request)
    {
                        //dd($request->all());
                        $this->validate($request, [
                            'name' => 'required|min:3',
                            'email' => 'required|email',
                            'password' => 'required',
                            'role' => 'required'
                        ]);
                
                        $user = User::create([
                            'name' => $request->name,
                            'email' => $request->email,
                            'password' =>bcrypt($request->password),
                            'role' => $request->role,
                            'status' => ('0')
                        ]);
                
                        return redirect()->back()->with('success','Data berhasil disimpan');
    }

